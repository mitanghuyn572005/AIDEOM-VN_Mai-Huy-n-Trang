def entropy_weights(X):
    # X: matrix of values (n_alternatives x n_criteria)
    # 1. Normalize
    col_sum = X.sum(axis=0)
    col_sum[col_sum == 0] = 1e-12
    P = X / col_sum
    # 2. Calculate Entropy
    k = 1.0 / np.log(len(X))
    # Add a small epsilon to avoid log(0)
    E = -k * np.nansum(P * np.log(P + 1e-12), axis=0)
    # 3. Calculate Weight
    d = 1 - E
    return d / d.sum()

