    # 2. Decision variables
    x1 = pulp.LpVariable("Infrastructure", lowBound=0) # Hạ tầng số
    x2 = pulp.LpVariable("AI_Data", lowBound=0)        # AI và Dữ liệu
    x3 = pulp.LpVariable("Human_Resources", lowBound=0)# Nhân lực số
    x4 = pulp.LpVariable("RD_Tech", lowBound=0)         # R&D công nghệ

    # 3. Objective function
    prob += 0.85 * x1 + 1.20 * x2 + 0.95 * x3 + 1.35 * x4, "Total_GDP_Gain"

    # 4. Constraints
    # Store constraints in variables to access dual values later
    c_total = x1 + x2 + x3 + x4 <= 100
    c_x1_min = x1 >= 25
    c_x2_min = x2 >= 15
    c_x3_min = x3 >= 20
    c_x4_min = x4 >= 10
    c_strategic = x2 + x4 >= 0.35 * (x1 + x2 + x3 + x4)

    prob += c_total, "Total_Budget"
    prob += c_x1_min, "Min_Infrastructure"
    prob += c_x2_min, "Min_AI_Data"
    prob += c_x3_min, "Min_Human_Resources"
    prob += c_x4_min, "Min_RD_Tech"
    prob += c_strategic, "Strategic_Tech_Share"

    # 5. Solve using the default solver (CBC)
    prob.solve(pulp.PULP_CBC_CMD(msg=0))
