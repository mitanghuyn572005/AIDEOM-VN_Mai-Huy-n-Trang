def solve_11_3_5(tabular_r):
    """
    Câu 11.3.5 (Mở rộng):
    Cấu trúc mạng: 2 hidden layers, mỗi lớp 64 units.
    """
    try:
        from stable_baselines3 import DQN
        from stable_baselines3.common.monitor import Monitor
    except ImportError:
        print("Lỗi: Chưa cài đặt stable-baselines3. Hãy chạy 'pip install stable-baselines3 shimmy'")
        return

    print("\n--- Câu 11.3.5 (Mở rộng): Deep Q-Network (DQN) ---")

    # Tạo môi trường
    env = VietnamEconomyEnv()
    rng = np.random.default_rng(42)
    # SB3 yêu cầu môi trường được bọc trong Monitor và DummyVecEnv để theo dõi
    env = Monitor(env)

    # Cấu hình mạng Neural (2 lớp ẩn, mỗi lớp 64 units)
    policy_kwargs = dict(net_arch=[64, 64])

    # Khởi tạo mô hình DQN
    model = DQN("MlpPolicy", env,
                policy_kwargs=policy_kwargs,
                learning_rate=1e-3,
                buffer_size=10000,
                learning_starts=1000,
                batch_size=64,
                gamma=0.95,
                train_freq=4,
                gradient_steps=1,
                target_update_interval=500,
                exploration_fraction=0.1,
                exploration_final_eps=0.05,
                verbose=0)

    print("Đang huấn luyện DQN (20,000 steps)...")
    model.learn(total_timesteps=20000)

    # Đánh giá DQN
    eval_episodes = 100
    dqn_rewards = []
    for _ in range(eval_episodes):
        obs = env.reset()
        ep_reward = 0
        while True:
            action, _states = model.predict(obs, deterministic=True)
            obs, reward, terminated, truncated, info = env.step(action)
            ep_reward += reward
            if terminated or truncated: break
        dqn_rewards.append(ep_reward)

    avg_dqn = np.mean(dqn_rewards)
    print(f"Phần thưởng trung bình của DQN: {avg_dqn:.2f}")

    # So sánh với kết quả Tabular (giả định từ 11.3.4 là 6.22)
    print(f"So sánh với Tabular Q-Learning: {tabular_r:.2f}")

    if avg_dqn > tabular_r:
        print("=> Kết luận: DQN CÓ cải thiện hiệu quả so với Tabular Q-Learning.")
    else:
        print("=> Kết luận: DQN chưa cho thấy sự cải thiện rõ rệt trong môi trường đơn giản này.")
        print("   (Lý do: Không gian trạng thái 81 là đủ nhỏ để Tabular học tối ưu hoàn hảo)")

