def solve_10_5_1():
    """
    Câu 10.5.1: Cài đặt mô hình bằng Pyomo.
    Sử dụng cấu trúc Set, Param, Var để mô hình hóa rõ first-stage và second-stage.
    """
    # 1. Khởi tạo Mô hình
    model = pyo.ConcreteModel()

    # 2. Định nghĩa Tập hợp (Sets) - Section 10.5
    model.J = pyo.Set(initialize=['I', 'D', 'AI', 'H']) # Hạng mục đầu tư
    model.S = pyo.Set(initialize=['s1', 's2', 's3', 's4']) # Kịch bản: Lạc quan, Cơ sở, Bi quan, Khủng hoảng

    # 3. Tham số (Parameters) - Bảng 10.2 và 10.4
    # Xác suất kịch bản
    prob_dict = {'s1': 0.30, 's2': 0.45, 's3': 0.20, 's4': 0.05}
    model.p = pyo.Param(model.S, initialize=prob_dict)

    # Hệ số hiệu quả GĐ 1 (β cơ bản)
    beta_base = {'I': 1.00, 'D': 1.10, 'AI': 1.25, 'H': 0.95}
    model.beta = pyo.Param(model.J, initialize=beta_base)

    # Hệ số hiệu quả GĐ 2 (β^s_j theo kịch bản)
    beta_s_dict = {
        ('s1','I'): 1.25, ('s1','D'): 1.35, ('s1','AI'): 1.55, ('s1','H'): 1.05,
        ('s2','I'): 1.00, ('s2','D'): 1.10, ('s2','AI'): 1.25, ('s2','H'): 0.95,
        ('s3','I'): 0.75, ('s3','D'): 0.85, ('s3','AI'): 0.90, ('s3','H'): 1.00,
        ('s4','I'): 0.40, ('s4','D'): 0.50, ('s4','AI'): 0.55, ('s4','H'): 1.10
    }
    model.beta_s = pyo.Param(model.S, model.J, initialize=beta_s_dict)

    # 4. Biến quyết định (Variables)
    # First-stage (here-and-now): x_j
    model.x = pyo.Var(model.J, within=pyo.NonNegativeReals)

    # Second-stage (recourse): y_sj
    model.y = pyo.Var(model.S, model.J, within=pyo.NonNegativeReals)

    # 5. Ràng buộc (Constraints) - Section 10.3
    # (C1) Ngân sách giai đoạn 1 <= 65.000
    model.budget1 = pyo.Constraint(expr=sum(model.x[j] for j in model.J) <= 65000)

    # (C2) Ngân sách giai đoạn 2 <= 15.000 cho mỗi kịch bản s
    def budget2_rule(m, s):
        return sum(m.y[s, j] for j in m.J) <= 15000
    model.budget2 = pyo.Constraint(model.S, rule=budget2_rule)

    # (C3) Ràng buộc AI GĐ 2 bị giới hạn bởi Nhân lực GĐ 1: y_AI^s <= 0.5 * x_H
    def ai_limit_rule(m, s):
        return m.y[s, 'AI'] <= 0.5 * m.x['H']
    model.ai_limit = pyo.Constraint(model.S, rule=ai_limit_rule)

    # 6. Hàm mục tiêu (Objective): Tối đa hóa GDP tăng thêm kỳ vọng
    def obj_rule(m):
        # Lợi ích GĐ 1
        first_stage = sum(m.beta[j] * m.x[j] for j in m.J)
        # Lợi ích kỳ vọng GĐ 2
        second_stage = sum(m.p[s] * sum(m.beta_s[s, j] * m.y[s, j] for j in m.J) for s in m.S)
        return first_stage + second_stage

    model.obj = pyo.Objective(rule=obj_rule, sense=pyo.maximize)

    # 7. Giải bài toán
    # Thử các solver theo thứ tự ưu tiên
    solver = get_solver()
    result = solver.solve(model)

    if result.solver.termination_condition != pyo.TerminationCondition.optimal:
        raise RuntimeError(
            f"Solver không tìm được nghiệm tối ưu: "
            f"{result.solver.termination_condition}"
        )

    # 8. Báo cáo kết quả
