# 1. Define the Environment
class VietnamEconomyEnv(gym.Env):
    """
    Môi trường MDP đơn giản hoá kinh tế Việt Nam (không cần gymnasium).
    Trạng thái: [GDP_growth, Digital_index, AI_capacity, Unemployment_risk]
    Mỗi chiều 3 mức: 0=thấp, 1=trung bình, 2=cao → 3^4 = 81 trạng thái
    """
    def __init__(self):
        self.action_space = spaces.Discrete(5)

        self.observation_space = spaces.MultiDiscrete(
            [3, 3, 3, 3]
        )
        self.n_actions = 5
        self.obs_shape = (3, 3, 3, 3)   # shape của Q-table
        self.T = 10

        self.allocation = {
            0: np.array([0.70, 0.10, 0.10, 0.10]),  # a0: Truyền thống
            1: np.array([0.40, 0.25, 0.15, 0.20]),  # a1: Cân bằng
            2: np.array([0.25, 0.45, 0.15, 0.15]),  # a2: Số hóa nhanh
            3: np.array([0.20, 0.20, 0.45, 0.15]),  # a3: AI dẫn dắt
            4: np.array([0.30, 0.20, 0.10, 0.40]),  # a4: Bao trùm
        }
        self.w = np.array([0.40, 0.25, 0.20, 0.15])
        self.rng = np.random.default_rng()

    def reset(self, seed=None):
        if seed is not None:
            self.rng = np.random.default_rng(seed)
        self.state = np.array([1, 1, 0, 1], dtype=np.int32)  # VN 2026
        self.t = 0
        self.K, self.D, self.AI, self.H = 27500.0, 20.3, 86.0, 30.0
        return self.state.copy(), {}

    def _compute_reward(self, s, action):
        """
        Reward phụ thuộc trạng thái – đảm bảo policy khác nhau theo context.
        R = w1*ΔGDP + w2*Δunemp_reduction - w3*CyberRisk - w4*Emission
        """
        a = self.allocation[action]
        gdp_s, digi_s, ai_s, unemp_s = s

        # GDP: K hiệu quả khi gdp thấp, AI hiệu quả KHI ai_cap cao (absorptive capacity)
        k_eff  = 1.6 - gdp_s * 0.35
        d_eff  = 1.2 - digi_s * 0.25
        ai_eff = 0.8 + ai_s * 0.5 + gdp_s * 0.15
        delta_gdp = a[0]*k_eff + a[1]*d_eff + a[2]*ai_eff + a[3]*0.7

        # Giảm thất nghiệp: H quan trọng hơn khi unemp cao; AI gây hại khi ai_cap thấp
        h_power  = 1.0 + unemp_s * 0.7
        ai_harm  = 0.4 * (2 - ai_s + 1) / (ai_s + 1)
        delta_unemp = a[3]*h_power - a[2]*ai_harm

        # Rủi ro cyber: AI nhiều + ai_cap thấp = nguy hiểm
        cyber = a[2] * (1.2 - ai_s * 0.25)

        # Phát thải: K và AI tốn điện nhiều
        emission = a[0]*0.30 + a[2]*0.20 + a[1]*0.08

        return (self.w[0]*delta_gdp + self.w[1]*delta_unemp
                - self.w[2]*cyber - self.w[3]*emission)

    def step(self, action):
        action = int(action)
        a = self.allocation[action]
        reward = self._compute_reward(self.state, action)

        # Cập nhật trạng thái (stochastic transitions)
        new_state = self.state.copy()
        probs_up   = [a[0]*0.4, a[1]*0.5, a[2]*0.5, 0.0]         # xác suất tăng bậc
        probs_down = [0.05, 0.05, 0.05, a[2]*0.2 - a[3]*0.15]    # xác suất giảm bậc (unemp)

        for dim in range(4):
            p_up   = max(0, min(0.5, probs_up[dim]))
            p_down = max(0, min(0.3, probs_down[dim]))
            r = self.rng.random()
            if r < p_up   and new_state[dim] < 2:
                new_state[dim] += 1
            elif r < p_up + p_down and new_state[dim] > 0:
                new_state[dim] -= 1

        self.state = new_state
        self.t += 1
        done = self.t >= self.T
        terminated = done
        truncated = False

        return (
            self.state.copy(),
            reward,
            terminated,
            truncated,
            {}
        )
# 2. Q-Learning Implementation
def solve_11_3():
    """
    Câu 11.3.2: Cài đặt Q-learning với các tham số:
    alpha = 0.1, gamma = 0.95, epsilon giảm từ 1.0 -> 0.05 qua 10,000 episodes.
    """
    env = VietnamEconomyEnv()
    rng = np.random.default_rng(42)
    # Khởi tạo Q-table với 0: (3, 3, 3, 3, 5) -> 81 trạng thái x 5 hành động
    Q = np.zeros((3, 3, 3, 3, 5))

    # Tham số học máy (Câu 11.3.2)
    alpha = 0.1   # Learning rate
    gamma = 0.95  # Discount factor
    episodes = 10000

    rewards_history = []

    print(f"Bắt đầu huấn luyện Q-Learning ({episodes} episodes)...")

    for ep in range(episodes):
        s, _ = env.reset()

        # Epsilon-greedy với epsilon giảm dần (1.0 -> 0.05)
        # Công thức giảm tuyến tính: eps = max(0.05, 1.0 - ep / (episodes * 0.8))
        epsilon = max(0.05, 1.0 - ep / 8000)

        total_r = 0
        while True:
            # Lựa chọn hành động (Epsilon-greedy)
            if rng.random() < epsilon:
                a = int(rng.integers(0, 5)) # Khám phá (Exploration)
            else:
                a = np.argmax(Q[tuple(s)])    # Khai thác (Exploitation)

            s2, r, terminated, truncated, _ = env.step(a)

            # Cập nhật Q-table theo công thức Bellman (Câu 11.3.2)
            # Q(s,a) = Q(s,a) + alpha * [reward + gamma * max(Q(s2, a')) - Q(s,a)]
            old_val = Q[tuple(s) + (a,)]
            next_max = np.max(Q[tuple(s2)])

            Q[tuple(s) + (a,)] = old_val + alpha * (r + gamma * next_max - old_val)

            s = s2
            total_r += r
            if terminated or truncated: break

        rewards_history.append(total_r)

        if (ep + 1) % 2000 == 0:
            print(f"Episode {ep+1}/{episodes} - Epsilon: {epsilon:.2f} - Last Reward: {total_r:.2f}")

    # ===== Lưu chính sách tối ưu =====
    policy = np.argmax(Q, axis=-1)

    np.save(
        "outputs/optimal_policy.npy",
        policy
    )

    print(
        "Đã lưu policy tại: outputs/optimal_policy.npy"
    )
    # ===== Lưu Q-table =====

    np.save(
        "outputs/q_table.npy",
        Q
    )

