class VietnamDigitalProblem(ElementwiseProblem):
    def __init__(self):
        super().__init__(n_var=24, n_obj=4, n_ieq_constr=16,
                         xl=np.zeros(24), xu=np.ones(24) * 50000)

        self.beta = np.array(
            [[1.15, 0.85, 0.55, 1.30], # NMM
            [0.95, 1.25, 1.40, 1.05], # RRD
            [1.05, 0.95, 0.85, 1.15], # NCC
            [1.20, 0.75, 0.45, 1.35], # CH
            [0.90, 1.30, 1.55, 1.00], # SE
            [1.10, 0.85, 0.65, 1.25]  # MD
        ])

        self.e = np.array([0.42, 0.55, 0.48, 0.32, 0.62, 0.38])
        self.rho = np.array([0.18, 0.45, 0.28, 0.12, 0.52, 0.22])
        self.sig = np.array([0.32, 0.28, 0.30, 0.35, 0.25, 0.30])

        self.D0 = np.array([38, 78, 55, 32, 82, 48])
        self.gamma = 0.002
        self.lam = 0.7

    def _evaluate(self, x, out, *args, **kwargs):
        X = x.reshape(6, 4)

        f1 = -np.sum(self.beta * X)
        regional_totals = np.sum(X, axis=1)
        f2 = np.mean(np.abs(regional_totals - np.mean(regional_totals)))
        f3 = np.sum(self.e * (X[:, 0] + X[:, 2]))
        f4 = np.sum(self.rho * X[:, 2]) - np.sum(self.sig * X[:, 3])

        out["F"] = [f1, f2, f3, f4]

        g = []
        g.append(np.sum(X) - 50000)
        for r in range(6):
            g.append(5000 - np.sum(X[r, :]))
        g.append(np.sum(X[1, :]) - 12000)
        g.append(np.sum(X[4, :]) - 12000)
        g.append(12000 - np.sum(X[:, 3]))

        D_final = self.D0 + self.gamma * X[:, 1]
        max_D = np.max(D_final)
        for r in range(6):
            g.append(self.lam * max_D - D_final[r])

        out["G"] = np.array(g)

