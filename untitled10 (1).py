def solve_1_4_3():
    # 1. Load data
    df = load_macro()
    years = df['year'].values
    Y = df['GDP_trillion_VND'].values

    # Data from Table in Section 1.3
    K = np.array([16500, 17800, 19600, 21300, 23500, 25900])
    L = np.array([53.6, 50.5, 51.7, 52.4, 52.9, 53.4])
    D = np.array([12.0, 12.7, 14.3, 16.5, 18.3, 19.5])
    AI = np.array([55.6, 60.2, 65.4, 67.0, 73.8, 80.1])
    H = np.array([24.1, 26.1, 26.2, 27.0, 28.4, 29.2])

    # Coefficients
    alpha, beta, gamma, delta, theta = 0.33, 0.42, 0.10, 0.08, 0.07

    # 2. Estimate TFP (A_t) for each year
    A = Y / (K**alpha * L**beta * D**gamma * AI**delta * H**theta)

    # 3. Calculate growth rates (delta ln X)
    def calc_avg_growth(X):
        # Calculate ln(X_t) - ln(X_{t-1}) for each year pair
        ln_growth = np.diff(np.log(X))
        return np.mean(ln_growth)

    # Average annual growth of GDP
    g_Y = calc_avg_growth(Y)

    # Growth of each factor
    g_K = calc_avg_growth(K)
    g_L = calc_avg_growth(L)
    g_D = calc_avg_growth(D)
    g_AI = calc_avg_growth(AI)
    g_H = calc_avg_growth(H)
    g_A = calc_avg_growth(A)

    # 4. Calculate contributions
    # Contribution = weight * growth_rate
    contrib_K = alpha * g_K
    contrib_L = beta * g_L
    contrib_D = gamma * g_D
    contrib_AI = delta * g_AI
    contrib_H = theta * g_H
    contrib_TFP = g_A

    # Total growth (sum of contributions should equal g_Y)
    total_g = contrib_K + contrib_L + contrib_D + contrib_AI + contrib_H + contrib_TFP
    identity_check = pd.DataFrame({
        "Metric":[
            "GDP Growth",
            "Sum Contributions",
            "Difference"
        ],
        "Value":[
            g_Y,
            total_g,
            abs(g_Y-total_g)
        ]
    })

    output_dir = Path("outputs")
    output_dir.mkdir(exist_ok=True)

    identity_check.to_csv(
        output_dir / "bai1_growth_identity_check.csv",
        index=False
    )

    print(f"\ng_Y thực tế      : {g_Y:.6f}")
    print(f"Tổng đóng góp    : {total_g:.6f}")
    print(f"Sai số           : {abs(g_Y-total_g):.10f}")
    # Percentage contribution
    perc_K = (contrib_K / g_Y) * 100
    perc_L = (contrib_L / g_Y) * 100
    perc_D = (contrib_D / g_Y) * 100
    perc_AI = (contrib_AI / g_Y) * 100
    perc_H = (contrib_H / g_Y) * 100
    perc_TFP = (contrib_TFP / g_Y) * 100

    # Create results table
    factors = ['Vốn (K)', 'Lao động (L)', 'Số hóa (D)', 'Năng lực AI', 'Nhân lực số (H)', 'TFP']
    contributions = [perc_K, perc_L, perc_D, perc_AI, perc_H, perc_TFP]
    absolute_contributions = [contrib_K, contrib_L, contrib_D, contrib_AI, contrib_H, contrib_TFP]

    summary = pd.DataFrame({
        'Yếu tố': factors,
        'Đóng góp (%)': contributions,
        'Đóng góp tuyệt đối (Δln)': absolute_contributions
    })

    output_dir.mkdir(exist_ok=True)

    summary.to_csv(
        output_dir / "bai1_growth_decomposition.csv",
        index=False
    )

    print(f"Tăng trưởng GDP bình quân hàng năm (2020-2025): {g_Y*100:.2f}%")
    print("\nKết quả phân rã tăng trưởng:")
    print(summary.to_string(index=False))

    growth_summary = pd.DataFrame({
        "Metric":[
            "Average GDP Growth"
        ],
        "Value":[
            g_Y*100
        ]
    })

    growth_summary.to_csv(
        output_dir / "bai1_gdp_growth_summary.csv",
        index=False
    )

    # 5. Plotting
    plt.figure(figsize=(12, 7))
    bars = plt.bar(factors, contributions, color=['#3498db', '#e74c3c', '#2ecc71', '#f1c40f', '#9b59b6', '#34495e'])

    plt.axhline(0, color='black', linewidth=0.8)
    plt.title('Phân rã đóng góp vào tăng trưởng GDP Việt Nam (2020-2025)', fontsize=14)
    plt.ylabel('Phần trăm đóng góp (%)', fontsize=12)

    # Add labels on top of bars
    for bar in bars:
        yval = bar.get_height()
        plt.text(bar.get_x() + bar.get_width()/2, yval + 0.5, f'{yval:.1f}%', ha='center', va='bottom', fontweight='bold')

    plt.grid(axis='y', linestyle='--', alpha=0.7)

    # Ensure the 'outputs' directory exists before saving
    output_dir = Path('outputs')
    output_dir.mkdir(parents=True, exist_ok=True)

    # Save the plot
    output_path = output_dir / 'growth_accounting.png'
    plt.tight_layout()
    plt.savefig(output_path)
    plt.show()
    plt.close()
    print(f"\nĐã lưu biểu đồ phân rã tăng trưởng tại: {output_path}")

