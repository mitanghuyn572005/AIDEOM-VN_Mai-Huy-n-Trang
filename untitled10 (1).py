    # Chúng ta tính Tiêu dùng C dựa trên ngân sách: C_t = Y_t - Sum(I_t)

    def objective(x):
        # x là vector đầu tư: [Ik_0..9, Id_0..9, Iai_0..9, Ih_0..9]
        Ik = x[0:T]
        Id = x[T:2*T]
