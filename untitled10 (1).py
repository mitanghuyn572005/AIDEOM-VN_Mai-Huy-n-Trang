def solve_deterministic(beta_recourse):
    """Hàm phụ giải bài toán xác định với hệ số recourse cố định"""
    model = pyo.ConcreteModel()
    model.J = pyo.Set(initialize=['I', 'D', 'AI', 'H'])
    beta_base = {'I': 1.00, 'D': 1.10, 'AI': 1.25, 'H': 0.95}
    model.x = pyo.Var(model.J, within=pyo.NonNegativeReals)
    model.y = pyo.Var(model.J, within=pyo.NonNegativeReals)
    model.budget1 = pyo.Constraint(expr=sum(model.x[j] for j in model.J) <= 65000)
    model.budget2 = pyo.Constraint(expr=sum(model.y[j] for j in model.J) <= 15000)
    model.ai_limit = pyo.Constraint(expr=model.y['AI'] <= 0.5 * model.x['H'])

    obj = sum(beta_base[j] * model.x[j] for j in model.J) + \
          sum(beta_recourse[j] * model.y[j] for j in model.J)
    model.obj = pyo.Objective(expr=obj, sense=pyo.maximize)

    solver = get_solver()
    result = solver.solve(model)

    if result.solver.termination_condition != pyo.TerminationCondition.optimal:
        raise RuntimeError(
            f"Solver không tìm được nghiệm tối ưu: "
            f"{result.solver.termination_condition}"
        )
    return {j: pyo.value(model.x[j]) for j in model.J}, pyo.value(model.obj)

